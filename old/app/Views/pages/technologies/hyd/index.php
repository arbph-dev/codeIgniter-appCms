            <h2>Généralités</h2>
            <div>
                <div>
                    <p>
                        Les fluides, gaz, eau, air, huile sont soumis aux mêmes lois physiques mais nécessitent des équipements différents.<br/>
                        L'eau stockée dans un réservoir exerce une pression, cette pression dépend de la hauteur de la colonne d'eau.<br/>
                        On peut soutirer l'eau du réservoir en ouvrant la vanne, selon que l'on ouvre plus ou moins la vanne le débit est plus ou moins important.<br/>
                        Le débit de vidange dépendra de deux paramètres, la pression induite par la hauteur de colonne d'eau et les pertes de charge que l'on crée selon l'ouverture de la vanne.<br/>
                    </p>
                    <p>
                        Les fluides ont des états : liquides ou gazeux liés à la pression et la température, l'eau surchauffée s'évapore et la vapeur se condense<br/>
                        On doit considéré ces états principalement pour des applications liés à l'emploi de l'eau surchauffée et la compression des fluides frigorifiques.<br/>
                    </p>
                    <p>
                        Les équipements mise en œuvre, pompes, vannes, réservoir, anti-bélier, clapet anti retour, casse vide sont choisis selon les caractéristiques des fluides.
                        Ces équipements sont raccordés par des conduites ou tuyauterie, les raccordmeents se font avec des connecteurs, des raccord à vis ou des brides.
                    </p>                        
                    <p>
                        Les fluides ont des caractéristiques chimiques qu'il faut également considérer<br/>
                        Les carburants et liquide inflammables sont mis en œuvre avec des précautions adaptés : raccord en bronze, mise à la terre.<br/>
                        Pour certains fluides des cycles aspiration, refoulement et inertage peuvent être nécessaire<br/>
                    </p>

                    <h3>Les pompes</h3>
                    <p>
                        On répartit les pompes en deux catégories.<br/>
                        <br/>
                        Les <strong>pompes centrifuges</strong> sont les plus répandues, plus simples. elles sont caractérisées par leur débit important et des pression de refoulement moins élevées.<br/>
                        <br/>
                        Les <strong>pompes volumétriques</strong>  ont des débits plus faible mais permettent d'atteindre des pressions plus importantes.
                    </p>

