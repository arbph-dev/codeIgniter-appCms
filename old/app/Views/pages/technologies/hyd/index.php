            <h2>Les pompes centrifuges</h2>
            <div>
                <div>
                    <p>
                        Ces pompes se rencontrent couramment dans de nombreuses applications.Selon le type d'installation les pompes auront des régimes de fonctionnement différents
                    </p>
                    <p>
                        la pompe centrifuge dispose d'un débit important mais de hauteur de refoulement limité.<br/> 
                        En 1689, le physicien Denis Papin invente la pompe centrifuge, aujourd'hui la plus utilisée au monde.<br>
                        <br> 
                        La pompe centrifuge fonctionne sur un principe simple : le liquide est amené au moyeu de la roue et projeté au moyen de la force centrifuge vers la périphérie des roues. <br> 
                        
                        La construction est relativement peu coûteuse, solide et simple.<br> 
                        Sa grande vitesse permet de connecter directement la pompe à un moteur.<br>
                        <br>  
                        L'efficacité énergétique nécessite des systèmes modulants ici un groupe motopompe intègre les fonctions de régulation, l'instrumentation et un variateur.<br>
                        <br> 
                        <b>76% du coût du cycle de vie d'une pompe correspond au coût de l'énergie</b>
                    </p>
             

                    <h4>Composants de la pompe centrifuge</h4>
                    
                    <p>
                        La pompe centrifuge est de construction simple.<br><br>  
                        La roue peut être démonté. Le diamètre de la roue influe sur les caractéristiques de la pompe<br><br>  
                        Les roulements sont remplacés périodiquement.<br><br>  
                        L'étanchéité peut être assuré par des tresses ou des garnitures mécaniques.<br>
                        Les garnitures sont dans des matériaux comme le graphite ce qui le rend très fragile.<br/>
                        <br/>
                        Les pompes à tresses sont généralement pourvues d'une évacuation pour l'eau.<br>
                        <br>  
                        
                    </p>

                    <h4>Pompe de relevage</h4>
                    Les pompes de relevage et pompe serpilliere sont des pompes simples que l'on trouve dans des regards ou des fosses de relevage
                    Selon les modèles elles sont équipées de flotteur

                    <h4>Pompe centrifuge multicellulaire</h4>
                    Il existe des pompes centrifuges dite multicellulaire qui sont en fait une série de pompe centrifuge ce qui permet d atteindre des pressions importantes.<br/>
                    Les pompes centrifuge peuvent être associé en série ou parallèle  comme les piles et batterie mais en respectant les caractéristiques de la pompe.<br/> 
                    Pour augmenter la hauteur manométrique on associer des pompes en série, les pompes centrifuges multicellulaires sont employés dans les process, les surpresseurs et les chaufferies vapeurs.<br/>
                    



                    <h4>Pompe à vis centrifuge</h4>
                    Hydrostal propose des pompes spécifiques adaptées à certains produits délicats : poisson vivants<br/>
                    Hydrostal propose des rendements hydrauliques allant jusqu'à 85 % et des passages libres garantissant une très bonne prise en charge des particules<br/> 
                    Les NPSH des pompes Hidrostal sont très bas. les risques de cavitation, de désamorçage sont moindres qu'avec les pompes centrifuges classiques<br/>

                    <h4>Circulateur</h4>
                    Ils s'utilisent dans des applications de chauffage et de climatisation. Les débits et hauteur manométriques sont moins importants due fait que ces équipements sont implantés dans des circuits fermés ou seul les perte de charge des conduites sont a considérer.<br/>
                    Les pompes de chauffage ou circulateurs, aujourd'hui les modèles à variateur monophasé s impose hormis sur des fortes puissances. Ces pompes peuvent être contrôlés par une régulation et communiquer avec une gtb Il existe des groupes double doté d'un automatisme assurant la permutation automatique des pompes pour repartir les heures de fonctionnement et effectuer une reprise sur défaut.<br/>
                    <br/>
                    Les caractéristiques des circulateurs; les débits et hauteur sont moindres que ceux des pompes centrifuges.<br/>
                    Le choix d'un circulateur nécessite des mesure sou une étude du réseau; dans le cas d'un remplacement on, pourra vérifier que le circulateur est adapté.<br/>
                    <br/>
                    Les circulateurs communiquent avec les réseaux de GTC et GTB, ils intègrent des fonctions de régulation voir de comptage d'énergie.<br/>

