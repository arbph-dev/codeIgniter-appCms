                    <h3>Pompe à membrane</h3>
                    La pompe à membranes électrique a une consommation d'énergie jusqu'à 5 fois inférieure à la pompe à membrane à air comprimé. La pompe à membranes électrique réduit les pulsations sans avoir recours à un amortisseur de pulsations. 
                    
                    <h3>Pompe péristaltique</h3>
                    La pompe péristaltique est une technologie massive, encombrante et cher. Nous avons l'habitude d'expliquer que le choix d'une pompe péristaltique doit être fait lorsque tout a  déjà été essayé et qu'aucune autre technologie ne fonctionne.

