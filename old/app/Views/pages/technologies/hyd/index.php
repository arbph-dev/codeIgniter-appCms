                    <h3>Pompe en charge</h3>
                    <p>
                        Dans cette configuration la hauteur de charge Hc "aide" la pompe à imposer une pression Hr.<br/> 
                        La HMT se réduit et le débit augmente<br/><br/>
                        HMT = Hg + pdc = ( Hr - Hc ) + pdc<br/>
                        En pratique c'est le montage a privilégier pour optimiser le fonctionnement de la pompe.<br/><br/>
                        Une pompe de sprinkler peut aspirer l eau du réseau de ville à 3 bar qui fournit 350 m3/h pour une hauteur de 80 m, La pression atteint alors 11bar sur le réseau incendie.<br/>
                        La pompe est employé en surpresseur, les pressions du réseau et de la pompe s'additionne.
                    </p>

                    <h3>Courbe de pompe</h3>
                    <p>
                        Les constructeurs fournissent les courbes des pompes. Ici 3 courbes représentent : <br/>
                        La HMT en fonction du débit, HMT=f(Q)<br/>
                        Le NPSH en fonction du débit<br/>
                        La puissance absorbée en fonction du débit<br/><br/>
                        On peut lire le rendement sur la courbe HMT=f(Q)<br/>
                        Le NPSH est la pression minimale à maintenir à l'entrée de la pompe pour éviter la cavitation.<br/>
                        Les pompes sont parfois équipés de manomètre à l'aspiration et au refoulement. La lecture des pressions permet de situer le point de fonctionnement de la pompe<br/><br/>
                        <b>Les pompes centrifuges ont un débit minimal, Qmin, visible sur la courbe HMT=f(Q).<br/>
                        On évite de travailler "en bout de courbe" ou sous le NPSH pour éviter la cavitation</b><br/><br/>
                        Les phénomènes de cavitation peuvent s'avérer dévastateur et réduisent significativement la durée de vie d'une pompe. Ces pompes sont constitués de pièce de fonderie en fonte ce qui rend le rechargement impossible et leur réparation compliqué…
                    </p>

                    <h4>Modélisation de la courbe H=f(q)</h4>
                    Les débitmètres sont des équipements couteux, on dispose de manomètre différentielle pour des prix moindres, ceux ci permettent d'estimer le débit de la pompe.<br/>
                    Dans certain cas on connait déjà la pression a l'aspiration par une mesure de niveau réalisé avec un transmetteur de pression.<br/>
                    Il suffit donc d'ajouter un transmetteur de pression pour estimer le débit, au perte de charge a l'aspiration près.<br/><br/>
                    On rappelle que pour l'eau une hauteur de 10,2m correspond a 1 bar et qu'une pompe centrifuge se porte mieux si elle est en "charge" à l'aspiration.<br/>
                    <br/>
                    
                    On utilise ici des équations du troisième degré qui seront assez précises si l on choisit la plage de fonctionnement adapté.<br/><br/>
                    Nous passons sur le détail de la résolution,Les formules établies il ne restent qu'a les appliquer.Un tableur nous aidera.<br/><br/>
                    Les points sont relevés sur la courbe, soyez précis choisir trois point dans le début le milieu et la fin de la plage de fonctionnement<br/><br/>
