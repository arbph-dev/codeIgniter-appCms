            <h2>Pompes volumétriques</h2>
            <div>
                <div>
                    <p>
                    <h3>Pompes volumétriques</h3>
                    La majorité des pompes volumétriques sont réversibles, le débit est indépendant de la pression mais dépendra de la puissance du moteur. Ces pompes peuvent atteindre des pressions élevées et sont équipées de bypass pour protéger les installations

                    <h3>Les pompes a engrenage externe</h3>
                    La pompe à engrenage externe est une pompe volumétrique rotative équipée d'un rotor (Engrenage externe) à l'intérieur duquel est entraîné un pignon excentré. 
                    Pour des transferts de fluide à très haute viscosité, la pompe sera entraînée par un moto-réducteur à vitesse lente. Pour des viscosités faibles la pompe à engrenage pourra être entraînée à 1500 Tr/min. 
                    Débit jusqu'à 250 m3/h
                    Pression jusqu'à 16 bar
                    Température jusqu'à 300 °C

                    <h3>Pompe a piston circonférentiel</h3>
                    La pompe à piston circonférentiel est conçue pour le pompage de produits fragiles sensibles au cisaillement, liquides ou visqueux, chargés de particules molles ou dures. 
                    
                    <h3>Pompe à piston excentré</h3>
                    Elle permet de transférer des liquides très fluides jusqu'à des produits visqueux jusqu'à 15 000 centistokes(fuel lourd, bitume, colle). En fin de pompage elle permet de comprimer l'air dans la tuyauterie  de refoulement et ainsi de chasser le liquide. 
