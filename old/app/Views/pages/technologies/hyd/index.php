            <h2>Exploitation des pompes centrifuges</h2>
            <div>
                <div>
                    <p>
                        Les pompes et circulateurs sont employées dans des circuits ouverts et fermés. Dans un circuit fermé la pression statique est négligée.
                        Pour les circuits ouverts on distingue les pompes fonctionnant en aspiration ou en charge.
                    </p>
             
                    <h3>Pompe en aspiration</h3>
                    <p>
                        On mesure la hauteur géométrique Hg entre le refoulement et l'aspiration.<br/>
                        Cette Hauteur géométrique et les pertes de charge imposent la pression délivrée par la pompe ou Hauteur manométrique Totale, la Hauteur manométrique Totale ou HMT se répartit entre<br/>
                        la hauteur à l'aspiration Ha<br/>
                        la hauteur au refoulement Hr<br/>
                        les pertes de charge dans le reseau<br/>
                    </p>                              
