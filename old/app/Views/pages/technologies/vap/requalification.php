        <section>
            <h2>Préparation de la requalification</h2>
            <div> 
                <div>
                    <p>
                        La préparation de la requalification comprend
                        <ul>
                            <li>L'inventaire des taches et la consultation des fournisseurs</li>
                            <li>La commandes des prestations et matériels</li>
                            <li>La planification des interventions</li>
                            <li>La préparation des raccordements: électricité, eau, air ,vapeur, condensat</li>
                            <li>Le suivi des commandes et la réception des matériels</li>
                            <li>L'implantation du générateur de secours</li>
                            <li>les raccordements avec diminution de la production</li>
                            <li>les essais de sécurité, les réglages de régulation</li>
                            <li>la réception de l'installation provisoire par l'organisme de contrôle</li>
                            <li>La formation du personnel autorisé a réaliser les tests de sécurité</li>
                        </ul>
                        Avant de planifier les interventions il faut s'assurer des délais auprès des fournisseurs.<br>
                        Une inspection  minutieuse des équipements est nécessaire pour relever les composants à remplacer et noter les réparations à effectuer.<br>
                        Ci contre une soudure endommagée, probablement par laminage, a conduit à remplacer les "manchettes" sur la vanne de régulation d'eau alimentaire<br>
                        <br>
                        Quelques points ont nécessité une attention particulière et quelques discussions.<br>
                        Les asservissements implantés ont posé des difficultés, aucune possibilité de réguler le niveau. Le personnel était "habitué" à un réglage manuel. La solution préconisée imposant le passage de câble fut abandonnée.<br>
                        Le réglage des régulations fut dégrossi et devait être peufiné avec les courbes de production sur le superviseur. Le conducteur devait gérer ses réglages.<br>
                        <br>
  
                        <h3>Générateur de secours</h3>
                        La requalification des deux générateurs de vapeur devait s'étaler sur 3 semaines avec une semaine de battement pour les surprises éventuelles.<br>
                        <br>
                        Les choses sérieuses commencent avec la manutention du généateur de location.<br>
                        Le conduit de cheminée est implanté.Les raccordements, préparés en amont, ne posent pas de difficultés.<br>
                        
                        <br>
                        La mise en service par le spécialiste commence. <br>
                        Ci contre Le bruleur du générateur de vapeur, on distingue la ligne gaz avec le bloc gaz et son contrôleur d'étanchéité.<br> 
                        A l'opposé l'armoire de contrôle et de commande. Le ventilateur en noir se trouve devant le bruleur.<br>
                        On teste les asservissements et le déclenchement des sécurités. Durant ces essais nous échangeons pour prendre en main les équipements.<br>                         
                        La chaudière est fonctionnelle, l'organimse de controle valid el'instalaltion du générateur de location. Le raccordement au réseau vapeur se fera le lendemain. <br>
                        <br>
                        La préparation est bien avancée<br>
                        Je forme ensuite le personnel à la conduite et leur fait réaliser la séquence de test.


                        <br>

                    </p>
