        <section>
            <h2>Requalification</h2>
            <div> 
                <div>
                La préparation et les raccordements sont terminés, le temps restant aurait permis au conducteur de peaufiner les réglages des consignes tout en disposant toujours de 2 générateurs si besoin.<br>
                Le balisage est installé le vendredi.<br>
                <br>
                On arrête le générateur. On l'isole des réseaux : vapeur, eau, condensat, électricité, air comprimé puis on procède aux consignations.<br>
                Le générateur doit refroidir, on ouvre les portes en face avant<br>
                <br>
                Les premiers éléments sont remplacés :
                •	contacteurs<br>
                •	pressostat gaz<br>
                •	pompe alimentaire<br>
                <br>
                A l'origine le bruleur ne disposait pas de contrôle d'étanchéité indispensable sur cette puissance.<br>
                Pour moderniser la ligne gaz le constructeur, a l'époque.., a choisi la solution tout pressostat sans évent.<br>
                On trouve souvent un bloc gaz, avec controleur d'étanchéité, plus simple et surtout plus fiable..<br>
                La remise en service a été assez compliqué. 

                <h3>Ouverture du générateur, coté feu</h3>
                
                Dés le lundi matin les soudeurs attaquent le ressuage. 4 tubes sont à revoir dont 2 à remplacés.<br>
                •	La soudure est meulé des deux cotés. <br>
                •	Le tube est sortie et découpé en allant.<br>
                •	Le nouveau tube de 6m est mis en place d'un tenant. <br>
                •	Il est coupé a longueur puis pointé.<br>
                •	Il est ensuite dudgeonné des 2 cotés. <br>
                •	Les tubes sont ressoudés sur les plaques tubulaires<br>
                •	Les tresses des portes sont remplacées après nettoyage<br>
                <br>
                Les soudeurs confirmés m'ont assurés être a même de faire le diagnostique après ressuage. l'organisme de contrôle voulait intervenir à ce moment,il facture chaque magnétoscopie...<br>
                Il est intervenu après réparation pour vérifier les soudures réparées.<br>
                <br>
                Le ressuage à fait apparaitre une fissure dans un tube à l'arrière du générateur.<br>
                On voit au dessus la trace d'une réparation précédente.<br>
                <br>
                La fissure étant prononcée le risque de rupture étant avéré on pouvait bouchonner ou remplacer le tube.<br>
                La fissure provenant d'un défaut du tube et ou des contraintes due a l'exploitation on a choisit de remplacer le tube.<br>
                Ce tube fera "ressort" pour la dilatation et fusible en évitant d'endommager la plaque tubulaire.<br>
                L'organisme de contrôle consulté a fin de conseil a valider le choix.<br><br>
                Il prit note des remarques quant au contrainte thermiques due a l'exploitation. Cycle de dilation rétractation due au variations importantes de charge<br>
                <br>
                Il faut encore préparer l'épreuve hydraulique du lendemain...

                <h3>Ouverture du générateur, coté eau</h3>
                Les composants de sécurité sont déposés, les rampes de pression sont vérifiées et nettoyées.<br>
                Les montures de niveau sont retirées ainsi que les niveaux autocontrôlés et la vanne de régulation<br>
                On ferme les piquages avec des brides pleines.<br>
                On laisse un évent et un piquage pour le remplissage.<br>
                On monte les nouvelles soupapes sur la chaudière et on contrôle le matériel<br>
                On est prêt pour l'épreuve hydraulique de l'après midi.<br>
                <br>
                L'épreuve hydraulique se fait à une pression de 1,5 x le timbre du générateur; soit 27 bar pour un générateur de 18 Bar.

                <h3>Epreuve hydraulique et magnétoscopie</h3>
                Après l'épreuve hydraulique, la magnétoscopie révélera tout de même un défaut mineure sur une réparation. L'inertage d'un tel volume n'est pas aisée.<br>
                La soudure reprise et une gracieuse magnétoscopie plus tard la chaudière est déclarée conforme.<br>
                <br>
                Maintenant il reste tout a remonter pour valider les sécurités avant l'arrêt du second générateur.<br>
                On vidange; on remonte et on met en eau.<br>
                <br>
                On rédige les documents: bon de travaux et procès verbal de requalification.<br>
                Les soudeurs ont finis leur semaine, bien chargé, et l'organisme de contrôle veut les certificats de tarage des nouvelles soupapes et le procès verbal des réparations

                <h3>Vérification des sécurités</h3>
                La dernière étape est la vérification des sécurités, cette vérfication s'est bien déroulée.<br>
                Le remplacement du pressostat de controle d'étanchéité gaz, a solutionné un défaut récurrent lors des tests

                <h3>Travaux et traçabilité</h3>
                Les équipements sous pression sont soumis a des réglementations spécifiques.<br>
                Chaque générateur doit disposer d'un dossier technique à jour. On doit retrouver une trace des réparations effectuées<br>
                Le dossier technique indique les réparations précédentes et le procès verbal de requalification celle réalisés lors de la requalification.<br>
                Ces documents sont archivés à la maintenance et transmis au service qualité pour enregistrement.<br>

                Le générateur est reparti pour 10ans...
