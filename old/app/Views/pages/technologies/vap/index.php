            <h2>Principes</h2>
            <div> 
                <div>
                    <p>
                        La vapeur est employée comme force motrice pour entrainer les turbines et fournir l'électricité. On l'emploie également en fluide thermique.<br>
                        La production de vapeur d'eau nécessite une importante quantité d'énergie, cette chaleur peut être récupéré sur les machines ou produite par des bruleurs.<br><br>
                        L'exploitation de la vapeur nécessite une formation; les niveaux de température et de pression mis en œuvre ne sont pas a négliger. Les systèmes doivent être maitrisés<br>
                        <br>
                        L'eau peut prendre trois états : solide, liquide ou gazeux selon le conditions de pression et de température.<br>
                        Selon ces conditions de pression et température on obtiendra de l'eau surchauffée, de la vapeur saturée ou de la vapeur surchauffée<br>
                        <br>
                        Les applications sont assez variées : stérilisation, cuisson, production d'électricité.<br>
                        <br>
                        Les réseaux de vapeur sont généralement des circuits fermés, de par le sprocess qui ne permettent pas de mélange de fluides et du fait qu'on essaie de récupérer l'énérgie et une eau de qualité. 
                        Des process sont dit à vapeur perdues.<br>
                        La vapeur produite par les générateurs, est acheminée par le réseau de distribution. Comme sur l'air comprimé on essaie de réaliser les piquages sur le dessus des conduites.<br>
                        Du fait de la dilatation on aménage des supportages avec des patins glissants, des lyres de dilatation sont indispensables sur les grandes longueurs.<br>Des purgeurs de ligne sont disposés régulièrement sur les reséaux<br>
                        <br>
                        On maitrise la température en ajustant la pression, on emploie des postes de détente. Ces postes de détente sont équipés de soupapes et délivre la vapeur aux utilités.<br>
                        Dans un atelier on peut trouver plusieurs barrilets vapeur selon les besoins<br>
                        <br>
                        Selon les process on utilise des vannes, des vannes thermostatiques avec un capillaire, des détendeurs ou des vannes de régulation pour controler la température, la pression voir le débit.<br>
                        <br>
                        La vapeur passe dans des cuves ( double paroi, serpentins) ou dans des echangeurs.<br>
                        Le principe de la thermodynamique s'applique la vapeur cède sa chaleur latente puis est évacué par les purgeurs<br>
                        <br> 
                        La vapeur a été utilisé et condensé, elle cède une partie de son énergie thermique sous forme de chaleur latente et se condense.<br>
                        Les condensats, de l'eau déminéralisé et chaude, qui disposent d'une chaleur sensible importante, sont évacués par des purgeurs puis renvoyés dans le circuit d'eau alimentaire.<br>
                        Les condensats sont remployés si leur qualité le permet et renvoyer avec l'eau d'appoint en bâche alimentaire.<br>
                        <br>
                        Pour produire la vapeur on devra disposer d'une eau de qualité et des équipements de traitements d'eau adaptés.<br>
                        <br>                                                
                        Les auxiliaires principaux seront les pompes alimentaires, les circulateurs, le ventilateur d'air comburant et le diverter si le générateur est exploitable en GVR.
                        Un poste de détente gaz est généralement dédié avec des détendeurs redondants.<br>
                        Un compresseur d'air peut s'avérer nécessaire pour les actionneurs des systèmes de contrôles et de commande<br>
                        L'instrumentation nécessaire implique des automatismes pour la gestion du process et des sécurités.


                    </p>
                </div>
