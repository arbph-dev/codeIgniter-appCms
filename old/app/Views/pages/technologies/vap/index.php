            <h2>Distribution</h2>
            <div> 
                <div>
                    Le rôle de la distribution est d'acheminer et de réguler la pression de vapeur.<br>
                    La production est protégé contre les surpressions par des sécurités électriques et des soupapes de sûreté.<br><br>
                    La vapeur est acheminée dans les barillets ou nourrice, on y trouve des vannes pour les arrivées depuis les générateurs de vapeurs , des vannes de sectionnement pour alimenter les réseaux et les détentes.<br><br>
                    Les postes de détente réduisent la pression et donc la température, ils sont protégés par une soupape de sureté. Les turbines utilisent la force motrice de la vapeur haute pression, on peut exploiter la vapeur basse pression rejetée par les turbines on parle alors de sous tirage, la aussi de soupapes sont nécessaires pour la protection des équipements<br><br>
                    Les échappements des soupapes peuvent être équipés de transmetteur pour signaler leur déclenchement.<br><br>
                    Les réseaux de vapeur nécessitent des piquages réalisés en partie supérieure des tuyauteries, des purgeurs de lignes et des vanne de bypass sont ajoutés pour éliminer l'eau due au perte ou lors des mises en service<br>
