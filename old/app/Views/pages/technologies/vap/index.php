            <h2>Production</h2>
            <div> 
                <div>

                    La vapeur est produite depuis de l'eau chauffée par des combustibles gazeux, liquides, solides ou de l'énergie électrique.<br>
                    L'énergie thermique peut être récupérée à travers des échangeurs notamment sur l'échappement des moteurs ou en sortie de turbine.<br><br>
                    La vapeur est produite dans des chaudières à tubes de fumées ou à tubes d'eau, on peut trouver des chaudières dite de récupération ou GVR en sortie de turbine.
                    
                    <h3>Générateur à tube de fumées</h3>
                    Ci contre un générateur à tube de fumées et ses équipements. Les fumées effectuent un triple parcours pour échanger le maximum d'énergie.<br>
                    Pour augmenter le rendement de la chaudière on utilise un economiseur sur la sortie de fumées.<br>
                    Selon les combustibles employées on pourra exploiter un condenseur et maximiser le rendement du générateur de vapeur<br><br>
                    Notez l'importance des équipements et de l'instrumentation.
                    Les sécurités sont pour certaines doublées, les sécurités sont controlées à une fréquence déterminée par le mode d'exploitation 
                    
                    <h3>Générateur à tube d'eau</h3>
                    Ces générateurs produisent de la vapeur surchauffée à des pressions supérieure à 40 Bar et 400° ce qui permet de faire tourner les turbines ou de transporter la vapeur<br> 
                    On peut soutirer une partie de la vapeur sur une turbine et la détendre pour alimenter d'autres systèmes en basse pression.<br>
                    <br>
                    Les chaudières à tubes d'eau sont constituées de tubes rempli d'eau et de deux ballons.<br>
                    L 'eau circule du ballon inférieure par les tubes d'eau jusqu'au ballon supérieure ou elle se vaporise.<br>
                    Du fait des différence de densité l'eau plus froide retourne au ballon inférieure.<br>
                    Sur le retour elle repasse dans un tube d'eau et se réchauffe avant d'arriver au ballon inférieure.<br>
                    Dans le ballon inférieure l'eau surchauffée se mélange à l'eau d'appoint, l'eau mélangée est réchauffée dans les tubes d'eau et vaporisée au ballon supérieur.<br>
