        <h1 class="w3-text-blue">ETN - Centrale électrique</h1>
        <p>
          La centrale de dispatch, située à Caudan dans le Morbihan, fut exploitée par le passé en période EJP.<br/>
          Volontaire de nature et désirant progresser techniquement je suis intervenu sur cette centrale électrique en saison 2012/2013.<br/>
        </p>        
        <p>      
          La centrale est composé de 5 moteurs diesel de 22000 ch, d'un poste haut tension HTA et des auxilaires. fournit 6.5 MWe en 20kV.<br/>
        </p>
        
        <h2 class="w3-text-blue">Description</h2>        
        <p>
          La production électrique se fait par le biais d'alternateurs. Les groupes électrogène, exploités en secours permettent de délivrer rapidement de forte puissance.
        </p>
