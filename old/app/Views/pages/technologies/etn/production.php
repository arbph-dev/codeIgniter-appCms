        <h2 class="w3-text-blue">Défaut groupe ou centrale</h2>
        <p>
          Tout défaut survenant, dés lors qu'il a une incidence directe sur la production, doit être communiqué à RTE qui gère la distribution de l’énergie sur le réseau électrique.<br/>
          Selon le défaut, sa cause et ses conséquences on devra essayer de relancer le ou les groupes en défaut avant de transmettre des informations sur  la production de la centrale à RTE<br/><br/>
          Reporter vous à la procédure : Informations impératives sur la production centrale pour plus de précisions au sujet des transmissions à réaliser<br/>
          <br/>
          On distingue les dérives ou anomalies non bloquantes des alarmes<br/>
          Les anomalies signalisent un défaut qu'il faut corriger, certaines anomalies préviennent qu'une alarme est imminente.<br/>
          Une alarme signale un défaut qui met la machine ou l'équipement en sécurité<br/>
          <br/>
          Analyser le défaut, déterminer en la cause et les conséquences afin de le corriger efficacement, durablement et sans risque pour les biens et les personnes.
          <br/>
          De par sa conception la centrale permet de palier à de nombreux défauts d'automatismes et permet une conduite manuelle des équipements.<br/>
          <br/> 
          En manuel : ne faites rien sans en être sur. En cas de doute s'abstenir et demandez assistance plutôt que d'encourir des risques ou d'endommager les équipements ce qui entraînera des pertes d'exploitation certaines.<br/> 
          <br/>
          Les procédures ci après on été validés et éprouvées, appliquez les <b>strictement</b>.
        </p>
