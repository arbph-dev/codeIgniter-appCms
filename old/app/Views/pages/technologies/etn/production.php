        <h2 class="w3-text-blue">Poste HTA</h2>
        <p>
          La centrale dispose d'un poste HTA. Il comporte les cellules de livraison, de comptage, le disjoncteur principale et un disjoncteur par groupe électrogène.
        </p>        
        
        <p>
          Les compteurs auxilaires et soutirage permettent de suivre la production.<br/>
          Les autres compteurs d'exploitation sont relevés sur les armoires auxiliaires, les armoires et pupitre des machines
        </p>  

        <p>
          Chaque cellule disjoncteur HTA est équipée de sa protection électronique.<br/>
          Ces protections sont complémentaires au protection BT implantés dans les armoires de groupe
        </p>        

