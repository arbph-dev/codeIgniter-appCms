    <div class="w3-row w3-padding-32" id="ETN001B">
      <div class="w3-twothird w3-container">
        <h2 class="w3-text-blue">Salle de contrôle</h2>
        <p>
          La salle de contrôle est isolée de la salle des machines et du poste HTA. On y retrouve les armoires électrique de commande et la gestion des alarmes. Le report d'alarme est assuré par un automate WIT et la commande à distance via un SOFREL
        </p>        
        
        <p>
          Les auxiliares sont alimentés depuis un transformateur 20 kV/400V de 630kVA et une armoire de distribution.<br/> 
          L'armoire de distribution alimentant les armoire centrale, groupes , auxiliaires , l'éclairage, le chargeur 48V et les alarmes.<br/> 
          L'armoire de commande des auxilaires controle les pompes de transfert carburant et huile, les chargeurs de batterie et les extracteurs de la salle des machines.
        </p>  

        <p>
          Chaque Groupe dispose de son armoire éléctrique. Cette armoire gère le groupe, ses sécurités et la régulation de l'alternateur. Les parametres, dérives et anomalies sont reportés en face avant.
        </p>        

        <p>
          L'armoire principale de la centrale permet de synchroniser les groupes entre eux puis de les synchroniser avec le réseau ENEDIS. Elle comporte différents commutateurs pour la conduite. Notez l'instructiion aposée sur l'armoire. 
        </p>        
