    <div class="w3-row w3-padding-32" id="ETN001F">
      <div class="w3-twothird w3-container">
        <h2 class="w3-text-blue">Essai à vide</h2>

        <h3>Préparation</h3>
        <p>
        Pour contrôler le bon fonctionnement des équipements il est impératif de réaliser régulièrement des essais. Ces essais doivent être réalisé sans débiter sur le réseau.<br/>
        Pour réaliser les essais à vide on suit la procédure ci dessous ,en cas d'anomalie différer l'essai.<br/>
        <ul>
          <li>Contrôler visuellement les niveaux d’eau des échangeurs eau/air en toiture</li>
          <li>Contrôler les niveaux d'huile des groupes</li>
          <li>Contrôler l'état des connexions sur les batteries</li>
          <li>Contrôler les préchauffages d'huile.</li>
          <li>Contrôler l’absence d’anomalies et d’alarmes sur les verrines des armoires</li>
          <li>Vérifier l’absence de personnel dans les locaux groupes et HT.</li>            
          <li>Vérifier que le commutateur <b>SELECTION COUPLAGE MANUEL soit sur la position P7 CENTRALE</b>.</li>
        </ul>    

        <h3>Essai</h3>
          
        Sur l’armoire <b>AUXILIAIRE</b> : Placer le commutateur <b>PERSONNEL sur PRESENT</b><br/>
        Sur l’armoire <b>COMMUNE</b> : Placer le commutateur <b>COMMANDE DISJONCTEUR CENTRALE GROUPE sur ARRET</b>.<br/>
        <i>Le voyant d’anomalie 5 <b>CENTRALE INDISPO EN AUTO</b> doit s’allumer, le disjoncteur de couplage centrale ne peut plus s’enclencher.</i><br/>
        Sur l’armoire <b>COMMUNE</b> : Placer le commutateur <b>TEST MARCHE sur MARCHE</b>.<br/>
        <br/>
        La séquence de mise en service débute, les groupes doivent démarrer. La synchronisation choisit automatiquement le premeier groupe disonsible comme référence, les autres groupes vont réguler vitesse et excitation puis les cellules doivent s’enclencher dés que les gropupes sont opérationnels.<br/>
        <br/>
        L’essai va durer quelques 120 s , pendant ce temps contrôler le bon déroulement de l’essai.<br/>
        Consigner toute anomalies ou alarmes apparues dans le livret d’intervention.<br/>
        <br/>
        Après deux minutes la centrale va s’arrêter et les voyants d’alarme 6 NON COUPLAGE CENTRALE et 9 ARRET EXTERNE vont s’allumer.<br/>
        <br/>

        <h3>Redisposer en automatique</h3>
        Sur l’armoire <b>COMMUNE</b> : Placer le commutateur <b>TEST MARCHE sur ARRET</b>.<br/> 
        Vérifier que le commutateur <b>SELECTION COUPLAGE MANUEL soit sur la position P7 CENTRALE</b>.<br/>
        Placer le commutateur <b>COMMANDE DISJONCTEUR CENTRALE GROUPE sur AUTO</b>.<br/>
        Presser le bouton <b>EFFACEMENT DEFAUT</b>. La verrine  ne doit plus afficher de défauts<br/>
        <br/>
        Consigner l'essai à vide dans le livret d'intervention.<br/>
        Appliquez les vérifications de la procédure <b>Attente en automatique</b> afin de vous assurer que la centrale puisse démarrer en cas de demande<br/>
        </p>
