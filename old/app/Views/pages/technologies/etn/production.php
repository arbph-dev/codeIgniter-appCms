    <div class="w3-row w3-padding-32" id="ETN001C">
      <div class="w3-twothird w3-container">
        <h2 class="w3-text-blue">Salle des machines</h2>
        <p>
          La salle des machines avec les 5 groupes éléctrogènes. Les groupes sont équipées d'un moteur et d'un alternateur, l'alernateur alimente le transformateur élévateur qui renvoie l'énergie à la cellule HTA puis vers le réseau ENEDIS. 
        </p>        
        <p>
          Les machines sont controlées par des régulateurs woodward qui controle l'injection des moteurs et l'excitation des alternateurs. Chaque machine disposent de son pupitre de contrôles. La température d'échappement des 4 turbos est intégré aux chaines de sécurité
        </p>
        <p>
          Les diesels ont besoin d'air frais pour fonctionner, l'air chaud est extrait de la salle des machines avec des ventilateurs. 
        </p>
