            <h2>Management et sécurité</h2>
            <div>
                <div>
                    <h3>Contexte initial</h3>
                        <p>
                        En charge de la maintenance des services généraux sur un site, j'ai rapidement identifié des tensions au sein de mon équipe, notamment avec un collègue exprimant des revendications professionnelles et une vision très personnelle de ce qu'est un véritable professionnel.
                        </p> 
                        <p>
                        Lors d'une visite semestrielle du système sprinkler par l'APAVE, je n'ai pas été informé de leur arrivée, alors que la procédure impose l'enregistrement des visiteurs auprès du bureau de maintenance. Surpris, j'ai cherché à comprendre et ai retrouvé mon collègue dans le local sprinkler, en grande discussion sans rapport avec le contrôle en cours. L'échange qui s'ensuivit a été tendu, et une autre altercation eut lieu quelques jours plus tard au bureau, menant à une intervention de notre responsable direct pour apaiser la situation.
                        </p>
                        <p>
                        Malgré cet incident, j'ai choisi de transformer cette crise en opportunité pour accompagner mon collègue dans une prise de responsabilité et favoriser une meilleure collaboration.
                        </p>
                    <h3>Une démarche constructive</h3>
                        <p>
                        Une fuite est apparue sur le réseau sprinkler, une situation critique compte tenu de la sécurité incendie du site. J'ai décidé de confier à mon collègue la responsabilité de piloter cette intervention. L'objectif était double : résoudre une situation technique complexe tout en offrant à ce collaborateur une opportunité de démontrer ses compétences professionnelles.<br/>
                        J'ai sollicité un chiffrage rapide de la part de Précisteel et ai fourni des délais confortables pour réaliser les travaux, tout en insistant sur la nécessité d'informer notre responsable en cas d'indisponibilité temporaire du réseau. Cependant, l'intervention n'a pas été menée à son terme : une partie du réseau est restée non opérationnelle et non purgée, sans qu'aucune alerte ne soit transmise à notre hiérarchie.
                        </p>
                    <h3>Implication de l'équipe</h3>
                        <p>
                        Quelques mois plus tard, un défaut intermittent est apparu sur le bus du système de détection incendie Cerberus, défaut localisé sur la boucle de bus en aval du local sprinkler. Alors que je revenais de ma ronde, à 16h28, j'ai entendu une alarme se déclencher dans mon bureau.<br/> 
                        J'ai patienté jusqu'à 16h30, espérant une réaction de mon collègue, qui était pourtant sur site quelques minutes plus tôt. Lorsqu'il s'est présenté, prêt à quitter son poste, son sourire a disparu en constatant que j'avais attendu, bras croisés, face à la situation. Cette absence de réaction face à une alerte critique a mis en évidence des lacunes dans son implication et son sens des priorités.<br/> 
                        Le boitier de bus à été remplacé par un autre que mon prédecesseur, électricien, avait su conserver
                        </p>
                    <h3>bilan de cette expérience</h3>
                        <p>
                        Malgré mes efforts pour instaurer un dialogue et accompagner ce collègue dans une montée en compétence, cette expérience a démontré qu'il n'était pas disposé à assumer les responsabilités attendues. L'incident a affecté l'ambiance équipe et remis en question la faisabilité d'une collaboration harmonieuse.
                        </p>
                        <p>
                        J'ai choisi de tirer des enseignements de cette situation, notamment sur la gestion des crises humaines dans un environnement technique exigeant. Cette expérience m'a permis de confirmer mes aptitudes à faire face à des situations complexes, tout en maintenant le cap sur les enjeux de sécurité et de performance collective. En fin de compte, j'ai pris la décision de poursuivre ma carrière dans un environnement plus en phase avec mes ambitions professionnelles et mes valeurs.
                        </p>

