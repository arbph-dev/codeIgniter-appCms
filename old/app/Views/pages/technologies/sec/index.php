            <h2>Informer les assureurs</h2>
            <div>
                <div>
                    <p>
                        On doit  informer les assureurs en cas d'indisponibilité des systèmes de sécurité incendie, notamment les sprinklers.
                    </p>
                    <h3>Formulaire R1 : Indisponibilité Totale</h3>
                    <p>
                    Le formulaire R1 est utilisé pour déclarer une indisponibilité totale du système de sprinklers. Cette situation survient lorsque le système est complètement hors service, rendant l'installation vulnérable aux risques d'incendie. Il est impératif de notifier l'assureur dans les plus brefs délais, généralement au moins 72 heures avant la mise hors service prévisible, et le plus tôt possible en cas de mise hors service non prévisible.
                    </p>
                    <h3>Formulaire R6 : Indisponibilité Partielle</h3>
                    <p>
                    Le formulaire R6 est destiné à signaler une indisponibilité partielle du système de sprinklers. Cela concerne des situations où une partie du système est défectueuse ou en maintenance, mais où le reste du système reste opérationnel. Bien que le risque soit réduit par rapport à une indisponibilité totale, il est tout de même essentiel d'informer l'assureur de cette condition afin de garantir la couverture appropriée en cas d'incident.
                    </p>
                    <h3>Procédure de Notification</h3>
                    <p>
                    Il est recommandé de contacter directement votre assureur pour obtenir les formulaires spécifiques à votre contrat et à votre situation. Les exigences peuvent varier en fonction des termes de votre police d'assurance et des réglementations locales. Assurez-vous de fournir toutes les informations requises et de respecter les délais de notification pour maintenir la validité de votre couverture d'assurance.
                    </p>                     
