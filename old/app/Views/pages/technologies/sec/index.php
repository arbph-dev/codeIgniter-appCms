        <section>
            <h2>Ronde de fin de poste</h2>
            <div>
                <div>
                    <p>
                    Dans le cadre des prises et fins de poste, il est impératif de réaliser une ronde des installations, d'autant plus que la gestion des alarmes est un domaine réservé et que je n'ai aucune garantie d'une redondance des transmissions.
                    </p>
                    <h3>constat</h3>
                    <p>Lors de mon passage dans le local sprinkler, j'ai constaté une situation à éviter : une plaque rigide obstrue l'entrée d'air du local sprinkler. Cette entrée d'air est indispensable pour fournir l'air comburant au moteur diesel de la motopompe.
                    </p>
                    <p>
                    Une alarme ayant été déclenchée, un collègue – responsable d'après l'entête de ses mails – a décidé d'obstruer cette entrée d'air comme solution temporaire au problème. Selon ses explications, cette alarme était liée à des rafales de vent mal orientées.
                    </p>
                    <h3>Analyse</h3>
                    <p>L'obstruction de l'entrée d'air du moteur diesel compromet directement le fonctionnement de la motopompe sprinkler. En cas d'incendie, cela pourrait provoquer un démarrage dégradé du moteur, voire un calage, rendant le dispositif de protection inefficace.
                    </p>
                    <p>Si le moteur cale ou subit des dommages (par exemple sur le turbo), l'alimentation en eau des sprinklers pourrait être insuffisante, aggravant les conséquences d'un incendie.</p>
                    <h3>Action corrective et recommandations</h3>
                    <p>
                    Retirer la plaque rigide obstruant l'entrée d'air. Informer la hiérarchie dans une démarche de sécurité et traçabilité.<br/>
                    <br/>
                    Contrôler la sonde incriminée, la calibrer et/ou la remplacer si elle est défectueuse.<br/>
                    Mettre en place un rappel des consignes sur les pratiques temporaires non conformes, qui pourraient compromettre la sécurité du site.<br/>
                    Renforcer la formation des équipes sur l'importance des installations critiques comme les sprinklers.
                    </p>
                    <h3>Conclusion</h3>
                    <p>L'encadrement a exprimé son attachement aux bonnes pratiques et à la sécurité. Cette anomalie a été notée. Cependant, ce type de discussion, bien que désagréable, est essentiel pour prévenir des risques industriels majeurs.
                    </p>
                    <p>
                    Comme je l'ai rappelé, il est préférable de faire face aux réalités des problèmes plutôt que de les maquiller. Dans une équipe, il est crucial d'agir avec transparence et rigueur, afin de respecter les engagements réglementaires et la confiance des parties prenantes.
                    </p>                    
