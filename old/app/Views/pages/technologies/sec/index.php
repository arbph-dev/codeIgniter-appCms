            <h2>Sécurité incendie</h2>
            <div> 
                <div>                                
                    <h3>Introduction à la sécurité incendie</h3>
                        <h4>Objectifs de la sécurité incendie</h4>
                            <ul>
                                <li>Protéger les personnes, les biens et l'environnement.</li>
                                <li>Garantir la continuité d'exploitation des installations.</li>
                            </ul>
                        <h4>Cadre réglementaire</h4>
                            <ul>
                                <li>Règlementation ERP (Établissements Recevant du Public) et IGH (Immeubles de Grande Hauteur).</li>
                                <li>Normes applicables : NF S 61-930, APSAD R1, R4, etc.</li>
                                <li>Codes du travail et de la construction.</li>
                            </ul>

                    <h3>Principes fondamentaux : prévention, détection et action</h3>
                        <h4>Prévention</h4>
                            <ul>
                                <li>Limitation des risques d'incendie par conception (matériaux, équipements conformes).</li>
                                <li>Plans d'évacuation, balisage et éclairages de sécurité.</li>
                            </ul>
                        <h4>Détection</h4>
                        <ul>
                            <li>Systèmes de Détection Incendie (SDI) : fonctionnement, technologies (boucles, détecteurs automatiques, déclencheurs manuels).</li>
                            <li>Alimentation par onduleur et interfaces avec la GTC ou télésurveillance.</li>
                        </ul>
                        <h4>Action</h4>
                        <ul>
                            <li>Moyens d'extinction : extincteurs, sprinklers, RIA (Robinets d'Incendie Armés).</li>
                            <li>Stratégies de désenfumage : soufflage, fermeture des arrivées d'air, ouverture des exutoires.</li>
                            <li>Levée de doute : rôle, méthodes, et réduction des risques de fausses alarmes.</li>
                        </ul>

                    <h3>Typologies des locaux et équipements adaptés</h3>
                        <h4>ERP et IGH : spécificités</h4>
                            <ul>
                                <li>Catégories d'ERP (1 à 5) et contraintes associées (effectifs, type d'activités).</li>
                                <li>Équipements spécifiques : sprinklers, extincteurs, détecteurs adaptés, désenfumage.</li>
                                <li>Rôle des commissions de sécurité : inspection, validation des installations.</li>
                            </ul>
                        <h4>Locaux industriels et techniques</h4>
                            <ul>
                                <li>Gestion des risques spécifiques (gaz, produits chimiques, sources de chaleur).</li>
                                <li>Adaptation des systèmes (détecteurs de gaz, systèmes à mousse).</li>
                            </ul>
                        <h4>Locaux sensibles : hôpitaux, data centers</h4>
                            <ul>
                                <li>Sécurisation des zones critiques (bus adressés, suppression de panique).</li>
                                <li>Systèmes redondants et gestion des interventions.</li>
                            </ul>



                    <h3>Exploitation : gestion des situations courantes et des alarmes</h3>
                        <h4>Intervention par points chauds (soudure, meulage, etc.)</h4>
                        <ul>
                            <li>Désactivation temporaire des zones concernées.</li>
                            <li>Surveillance active : présence d'un agent, extincteurs à proximité.</li>
                        </ul>
                        <h4>Gestion des alarmes feu réelles et fictives</h4>
                        <ul>
                            <li>Feu réel : évacuation, validation par la CMSI, assistance des secours.</li>
                            <li>Feu fictif : levée de doute, analyse de la source de déclenchement.</li>
                        </ul>
                        <h4>Dérangement des systèmes (SDI, CMSI, Sprinkler)</h4>
                        <ul>
                            <li>Identification rapide de la panne et zones impactées.</li>
                            <li>Prévenir les assureurs pour tout dérangement significatif.</li>
                            <li>Mise en place de rondes de sécurité (fréquence adaptée au risque).</li>
                        </ul>
                        <h4>Conséquences sur l'exploitation</h4>
                        <ul>
                            <li>Impact des désactivations partielles ou totales (arrêts de production, délais d'intervention).</li>
                            <li>Coordination avec les services d'assurance et la direction.</li>
                        </ul>
                        <h4>Communication avec les assureurs et mise en place de rondes</h4>
                        <ul>
                            <li>Notification immédiate en cas de dérangement prolongé ou critique.</li>
                            <li>Organisation des rondes : fréquence, agents qualifiés, relevés des observations.</li>
                        </ul>

                        <h3>Formation et qualifications nécessaires</h3>
                            <h4>Qualifications liées à la sécurité incendie</h4>
                            <ul>
                                <li>SIAP (Service Incendie et d'Assistance à Personnes) niveaux 1 à 3.</li>
                                <li>Habilitations spécifiques pour l'exploitation des SSI et des réseaux sprinklers.</li>
                            </ul>
                            <h4>Formation pour la maintenance des sprinklers</h4>
                            <ul>
                                <li>Tests hebdomadaires : pompes, vannes, déclencheurs manuels.</li>
                                <li>Vérifications semestrielles et annuelles selon APSAD R1.</li>
                            </ul>

                        <h3>Maintenance et contrôles réglementaires</h3>
                            <h4>Fréquences et types de maintenance</h4>
                            <ul>
                                <li>SDI : test des détecteurs (mensuel/annuel).</li>
                                <li>CMSI : contrôle des alimentations, alarmes et sorties.</li>
                                <li>Sprinklers : maintenance des pompes, essais de débit et pressions.</li>
                            </ul>
                            <h4>Conformité et inspections</h4>
                            <ul>
                                <li>Interventions des commissions de sécurité : périodicité et rapports.</li>
                                <li>Vérifications par organismes agréés (APSAD, assureurs).</li>
                            </ul>
                            <h4>Documentation et traçabilité</h4>
                            <ul>
                                <li>Tenue à jour des registres de sécurité.</li>
                                <li>Archivage des rapports d'essais et interventions.</li>
                            </ul>


