                <h2>Exigences réglementaires</h2>
                <div>
                    <div>
                        <h3>Usages résidentiels</h2>
                        <h4>Température de l'eau</h4>
                            Température de production : minimum 60 °C pour éviter le développement de légionelles.<br/>
                            Température de distribution : minimum 50 °C aux points de puisage.<br/>
                        <h4>Contrôle des risques sanitaires</h4>
                            Maintien d'un débit suffisant pour éviter la stagnation.<br/>
                            Limitation de la température maximale à 50-55 °C aux points de puisage pour éviter les brûlures (norme NF P52-703).<br/>
                        <h4>Matériel adapté</h4>
                            Utilisation de robinets thermostatiques dans les salles de bains.<br/>
                            Contrôle périodique des dispositifs anti-brûlures.<br/>
                    
                        <h3>Usages tertiaires (bureaux, hôtels, écoles, commerces)</h2>
                        <h4>Température de l'eau</h4>
                            Production : minimum 60 °C.<br/>
                            Distribution : minimum 55 °C pour limiter les risques de légionellose.<br/>
                            Température aux points de puisage : limitée à 50 °C dans les douches et lavabos (prévention des brûlures).<br/>
                        <h4>Analyses légionelles</h4>
                            Obligatoires dans les établissements recevant du public (ERP).<br/>
                            Mesures régulières en sortie de production et aux points les plus éloignés.<br/>
                        <h4>Matériel adapté</h4>
                            Réseaux correctement dimensionnés pour limiter les zones de stagnation.<br/>
                            Pommeaux de douche démontables et nettoyables pour réduire les biofilms.<br/>
                        
                        <h3>Usages hospitaliers et médicaux (cliniques, maisons de retraite)</h2>
                        <h4>Température de l'eau</h4>  
                            Production : minimum 60-65 °C.<br/>
                            Distribution : minimum 55 °C dans les canalisations.<br/>
                            Maintien de l'eau chaude circulante à 50 °C minimum.<br/>
                        <h4>Analyses légionelles</h4>
                            Contrôle mensuel des températures (points de production, distribution, retour).<br/>
                            Recherche de légionelles tous les 3 mois (cf. arrêté du 1er février 2010 pour les établissements de santé).<br/>
                        <h4>Opérations complémentaires</h4>
                            Traitement des réseaux avec des produits désinfectants (biosafe, choc thermique).<br/>
                            Utilisation de filtres spécifiques pour les patients immunodéprimés.<br/>
                            Pommeaux et becs démontables pour entretien fréquent.<br/>
