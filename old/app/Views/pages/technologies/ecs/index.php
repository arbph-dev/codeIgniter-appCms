# Mitigeurs
## Mitigeurs et choc thermique

Les mitigeurs sont équipés de **dispositifs de sécurité** pour :

1. **Limiter la température maximale de sortie** (généralement réglée à 50-55 °C pour prévenir les brûlures).
2. **Arrêter le débit en cas de défaillance de l'arrivée d'eau froide** (sécurité anti-brûlure).

#### Problèmes des mitigeurs pour le choc thermique

- Les mitigeurs **ne permettent pas directement de réaliser un choc thermique**, car leur sécurité anti-brûlure bloque l'accès à l'eau à haute température.
- Même si la température de l'eau chaude est augmentée à 70-75 °C dans le réseau, le mitigeur mélangera l'eau chaude avec de l'eau froide, empêchant ainsi la température nécessaire d'être atteinte au point de puisage.

#### Conclusion

Les mitigeurs standards, équipés de sécurités anti-brûlures, **ne permettent pas de réaliser un choc thermique efficace** sans intervention spécifique. Pour les bâtiments nécessitant des chocs thermiques réguliers (tertiaires et hospitaliers), il est recommandé de :

- Installer des systèmes de purge dédiés.
- Choisir des mitigeurs adaptés aux réseaux d'eau chaude soumis à des traitements thermiques réguliers.

