            <section>
                <h2>Maintenance</h2>
                <div>
                    <div>
                        <h3>Contrôles des températures</h2>
                        <p>
                        Vérifier les températures à la sortie des ballons, en distribution et en retour.<br/>
                        Enregistrements réguliers pour tracer la conformité.<br/>
                        </p>
                    
                        <h3>Chocs thermiques</h2>
                        Réaliser périodiquement un choc thermique pour atteindre 70 °C dans tout le réseau.<br/>
                        Maintenir cette température pendant 30 minutes pour éliminer les légionelles.<br/>
                        <br/>
                        Le choc thermique est une méthode préventive ou curative utilisée dans les réseaux d'eau chaude sanitaire pour éliminer les bactéries, notamment les légionelles. Il consiste à élever la température de l'eau dans l'ensemble du réseau pendant une durée déterminée afin de détruire les micro-organismes sensibles à la chaleur.<br/>
                        <br/>
                        Étapes du choc thermique :<br/>
                        <h4>Montée en température</h4>
                        La température de l'eau dans le réseau est portée à 70-75 °C.<br/>
                        Cette température doit être maintenue pendant 30 minutes au minimum dans tous les points du réseau.<br/>
                
                        <h4>Purge et circulation</h4>
                        Les robinets et points de puisage sont ouverts progressivement pour s'assurer que l'eau chaude atteint tous les tronçons du réseau.
                        L'eau doit circuler à cette température pour couvrir les zones stagnantes et éviter les poches froides.<br/>
                
                        <h4>Rinçage</h4>
                        Après le choc thermique, les robinets sont rincés pour évacuer les résidus potentiels (biofilm détaché, dépôts).
                
                        <h4>Conditions de réussite</h4>
                        Le réseau doit être bien isolé pour limiter les pertes de chaleur.<br/>
                        L'eau doit atteindre la température cible dans les points les plus éloignés et les points bas du réseau.<br/>
                        Les équipements sensibles doivent être protégés (vannes thermostatiques, matériaux limitant les hautes températures).<br/>
                
                        <h3>Dépose et entretien des manchettes</h3>
                        Déposer et nettoyer les manchettes pour enlever les biofilms et dépôts calcaires.<br/>
                        Fréquence : trimestrielle en milieu sensible (hospitalier), semestrielle pour les autres usages.<br/>
                        
                        <h3>Nettoyage des équipements</h3>
                        Pommeaux de douche et aérateurs : démonter, nettoyer et désinfecter régulièrement pour éviter la prolifération bactérienne.<br/>
                        Échangeurs thermiques : détartrer selon la dureté de l'eau, au moins une fois par an.<br/>
                        
                        <h3>Surveillance des installations</h3>
                        Contrôle visuel des points sensibles (stagnation, dépôts).<br/>
                        Vérification du bon fonctionnement des dispositifs de régulation et des vannes.<br/>
