            <h2>Résolution d'un litige</h2>
            <div>
                <div>
                    <h3>Contexte</h3>
                    Dans mon activité de responsable de la maintenance, j'ai repris le dossier de mise en conformité pour l'exploitation SPHP du générateur de vapeur gaz.<br>
                    Le dossier était une priorité pour ma direction; les travaux on pris du temps. Le contexte était assez tendue et je découvrais encore la vapeur.<br>
                    Notre responsable de maintenance exploitait des cuves au dela de la pression de service. Un collègue venu pour prendre sa succesion, plus sensible et compétent sur cette technologie m'a permis d'évoluer. Malheureusement il nous a quitté.<br> 
                    
                    <h3>Mise en conformité</h3>
                    Au départ du responsable de maintenance j'ai assumer la maintenance<br>
                    J'ai remis le détendeur et ajouté une soupape sur le réseau, évidemement cela a modifié certaines habitude mais les équipements ont des pressiosn de service qu'on ne peut ignorer.<br>
                    <br>
                    En parallèle j'ai repris le dossier exploitation SPHP. L'APAVE nous conseillant, elle nous a fourni une liste des taches à réaliser.<br>
                    <br>
                    Les alarmes et dérives doivent remonté par un transmetteur, TBC light, à la télésurveillance<br>
                    Une détection gaz devait être implanté et des asservissments devaient couper les éneriges éléctrécité et gaz.<br>
                    La sonde autocontrolée de niveau bas délenche une alarme, l'APAVE demandait a se qu'elle soit raccourcie pour des raisons d'exploitation.<br>
                    Le programme de l'automate devait etre revue.<br>
                    La bache alimentaire devait etre équipé de niveau autocontrolée<br> 
                    <br>
                    Découvrant une nouvelle technologie j'ai du me reposer sur l'interface du prestataire pour identifier la sonde. Je pensais qu'il serait le plus à meme de m'aider.<br>

                    <h3>Travaux</h3>
                    En aout, l'usine était à l'arrêt. Seul, je réalisais :<br> 
                        Le remplacment des joints sur le soupapes<br>
                        L'ajustement de la sonde autocontrolée LSL (alarme niveau bas)<br>
                    <br>
                    En contrôlant mon travail; j'ai constaté que c'était la sonde LSLL déclenchant l'alarme niveau très bas que j'avais modifié.<br>
                    Ceci n'as pas eu d'impact sur le fonctionnement immédiat mais rendait une exploitation déja délicate encore moins raisionnable.<br>
                    Cela m'a contraint à commander rapidement une nouvelle sonde et a planifié une autre intervention.<br>
                    <br>
                    En aout je ne put passer la commande ; cela induit un retard supplémentaire.<br>
                    Cette éxpérience m'a servi de leçon mais n'a pas renforcé ma confiance dans l'interlocuteur de notre prestataire.


