            <h2>Correction du Volume gaz</h2>
            <div>
                <div>
                
                    Les fluides occupent un volume V, variable, selon la température T et la pression P.<br/>
                    <br/>  
                    On corrige le Volume de gaze V2 à une pression P2 et une température T2 donnée pour le ramener à un volume normal V1 en Nm3, dans des conditions normales de pression P1 et de température T1<br/>
                    <br/>
                    
                    On utilise une relation physique élémentaire.<br/>
                    <br/>
                    <blockquote>  
                        ( P1 x V1 ) / T1 = ( P2 x V2 ) / T2 = cte<br/>
                        donc <br/>
                        V1 = V2 x ( P2 / P1 ) x ( T1 / T2)<br/>
                    </blockquote>  

                    <h3>facteur de correction</h3>
                    Le volume de gaz V2 est compté en m3 (mètre cube) par un compteur volumétrique , sous pression une pression P2 une température T2<br/>
                    Pour les bilans énergétiques et la facturation on utilise le Volume V1 en Nm3 (normo mètre cube) à une pression P1 et une température  T1 , toutes deux normalisées<br/>
                    <br/>
                    En pratique un facteur de correction est utilisé, il figure sur les factures des fournisseurs<br/>
                    <br/>
                    <blockquote>  
                        F = ( P2 / P1 ) x ( T1 / T2 )<br/>
                        et <br/>
                        V1 = F x V2<br/>
                    </blockquote> 
                
