                        <h4>conversion de pression P2</h4>
                        Le gaz est distribué à P2 = 300 mBar. P2 est une pression relative, exprimé en Bar.<br/>
                        <br/>
                        Il faut déterminer la pression absolue P2 en Pa, on détermine la pression absolue en bar puis on la convertit en Pa<br/>
                        <br/>
                        La pression relative est mesurée par rapport à la pression atmosphérique, qui est prise comme référence ( 0 Bar relatif au niveau de la mer).<br/>
                        La pression absolue est la pression physique réelle soit 1 Bar absolue au niveau de la mer.<br/>
                        <strong>La pression absolue est la pression relative augmenté d'une pression d'une atmosphère</strong> soit 1 Bar ou 101 325 Pa.<br/>
                        <br/>
                        La pression absolue vaut 0 bar pour le vide absolu, la pression relative est alors de -1 Bar.<br/>
                        Au niveau de la mer, la pression absolue vaut 1 Bar , la pression relative est de 0 bar<br/>
                        <br/>
                        On lit 300 mbar sur un manomètre affichant la pression relative<br/>
                        La pression absolue est pression relative + pression athmopshérique donc P absolue = 0,3 + 1 soit 1,3 Bar.<br/>
                        <br/>
                        
                        
