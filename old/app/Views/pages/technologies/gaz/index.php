            <h1>Maintenance gaz</h1>
            <p>
            La mise en oeuvre des combustibles et combustibles gazeux est strictement encadrée.<br>
            Les normes évoluent, le téflon est proscrit sur les raccords. C'est loin d'être une évidence pour de nombreux techniciens.<br> 
            Une maintenance rigoureuse prévient de nombreux problèmes. Le personnel doit être formé et habilité comme pour l'électricité. 
             </p>
        </header>

