            <h2>Réglementation gaz</h2>
            <div>
                <div>
                <h3>Réglementation gaz en cusine</h3>
                Dés lors qu'un équipement gaz est ipmlanté dans une cusine :<br>
                Des entrées d'air suffisantes sont réalisés en partie haute et basse, les portes sont détalonnées pour permettre la circulation d'air et éviter la production de monoxyde de carbone<br>
                En présence d'un appareil à bruleur athmopshérique, les bouches de VMC et hottes d'extraction avec une évacuation exterieure sont interdites<br>
                <br>
                Les flexibles gaz des gazinières et appareils de cuisson sont pour certains à remplacer régulièrement.<br>
                

                <h3>Réglementation gaz en chaufferie</h3>
                <p>
                La classification des chaufferies se fait selon leur puissance, par puissance on entend la puissance installée au bruleur
                </p>

                <h4>Chaufferie &lt; 70 kW</h4> 
                    <p>Considérées comme "petites chaufferies", elles relèvent du <b>règlement général</b> et ne nécessitent pas d'autorisations spécifiques.</p>
                    <ul>
                    <li>Ventilation : aérations hautes et basses obligatoires.</li>
                    <li>Équipement gaz : robinet de coupure gaz manuel, limiteur de pression.</li>
                    </ul>

                <h4>Chaufferie entre 70 kW et 400 kW</h4>  
                    <p>Réglementées par l'arrêté du <b>23 juin 1978 modifié</b> (installations gaz).</p>
                    <ul>
                    <li>Ventilation renforcée (surfaces d'aération définies selon la puissance).</li>
                    <li>Détection de fuite de gaz obligatoire avec dispositif de coupure automatique.</li>
                    <li>Évacuation des produits de combustion via conduit dédié.</li>
                    </ul>
                
                <h4>Chaufferie > 400 kW</h4>  
                    <p>Ces installations sont soumises à des règles plus strictes relevant du <b>Code de l'Environnement** (rubrique 2910 des ICPE)</b>.<p>
                    <ul>
                    <li>Ventilation importante : grilles de ventilation haute et basse obligatoires</li> (surface calculée selon puissance).
                    <li>Détecteurs de gaz avec électrovanne de coupure gaz.</li>
                    <li>Régulation renforcée et dispositifs anti-retour de flamme.</li>
                    </ul>

                <h3>Emplacement spécifique</h3>  
                    <h4>Toiture</h4>
                    <ul>
                    <li>Conformité aux normes anti-soulèvement (vent).</li>
                    <li>Protection contre les chocs mécaniques (garde-corps, barrières).</li>
                    <li>Ventilation naturelle assurée par l'environnement extérieur.</li>
                    </ul>
                    
                    <h4>Sous-sol</h4>
                    <ul>
                    <li>Ventilation naturelle impossible, il faut une ventilation mécanique contrôlée.</li>
                    <li>Système d'extraction des fumées et des gaz en cas d'incident.</li>
                    <li>Interdiction des conduites souples pour le gaz.</li>
                    </ul>

