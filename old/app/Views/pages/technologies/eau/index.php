            <h2>cycle de fonctionnement des échangeurs ioniques</h2>
            <div>
                <div>
                    Le cycle de ses échangeurs comprend 5 phases : </p>
                    <ul style="padding-left: 48px;">
                        <li>la production</li>
                        <li>le soulèvement</li>
                        <li>la régénération</li>
                        <li>le rinçage lent</li>
                        <li>le rinçage rapide</li>
                    </ul>

                    <br/>
                    La phase de production est la phase de fonctionnement normal durant laquelle l'échangeur traite l'eau bute en entrée pour en retirer certaines espèces ioniques.<br/>
                    Un adoucisseur retient le calcaire lié au calcium et magnésium et relâche du sodium.<br/>
                    <br/>
                    Le soulèvement, permet de détasser les résines, les résines soumises à la pression se sont tassées durant la production cette phase permet de créer des espaces entre les résines ce qui assure un meilleur passage au régénérant en augmentant les surfaces d'échanges.<br/>
                    <br/>
                    Certains introduisent une phase de repos entre le soulèvement et la régénération.<br/>
                    <br/>
                    La régénération, durant cette phase on évacue les espèces ioniques retenus par les résines ;le régénérant prenant leur place.<br/>
                    On disitingue les régénérations à co-courant et à contre courant plus efficace<br/>
                    Pour un adoucisseur le calcium est relâche par les résines qui se chargent en Sodium.<br/>
                    <br/>
                    Le rinçage lent, dans cette phase on permet au régénérant de traverser toutes les couches de résines.<br/>
