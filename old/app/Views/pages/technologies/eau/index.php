        <h2>Constante de solubilité Ks</h2>
        <div>
            <div>
                <h3>sulfate de calcium</h3>
                
                Comme le sel de mer peut saturer une saumure, le sulfate de calcium en exces precipite et forme du platre.<br>                
                Le platre précipite quant sa concentration devient supérieure à la constante de solubilité Ks<br>
                <br>
                Cette constante s'exprime ainsi pour une eau pure ou exempte de calcium et de sulfates<br>  
                Le produit des concentrations Ks, est donné pour une températrure de référence.<br>
                <br>
                <blockquote>
                    Ks= [ Ca 2+ ] * [SO4 2-] = 0,0000314
                </blockquote>
                
                <br>
                Pour éviter la précipitation de CaSO4 il ne fau pas dépasser une concentation de SO42- tel que :<br>
                <blockquote>
                    [SO4 2-] = Ks / [ Ca 2+ ]
                </blockquote>

                <h3>eau d'alimentation</h3>
                Maintenant nous reprenons nos données :<br>
                <br> 
                La concentration de calcium et de magnésium est de : 30°f équivalent à  30/5000 = 0,006 eq/L.<br>
                La valence des ions Ca2+ et Mg2+ vaut 2.<br>
                On a donc 2 fois plus de charge électronique que de matière donc<br>
                <blockquote>
                    [ Ca 2+ ] + [ Mg 2+ ] = 0,003 mol/L<br>
                </blockquote>
                <br>
                On considère que [ Ca 2+ ] =4/5 du TH total soit [ Ca 2+ ] = 0,0024 mol/L<br>
                <br>
                Pour éviter la précipitation de CaSO4 il ne faudrait pas dépasser une concentration de SO42- tel que :<br>
                <blockquote>
                    [SO4 2-] = Ks / [ Ca 2+ ] = 0,0000314 / 0,0024 = 0,013 mol/L
                </blockquote>
                <br>
                Dans l'eau brute on observe une concentration de [SO4 2-] de 30 mg/L<br>
                Soit une concentration de 0,03 /96 = 0,0003125 mol/L.<br> 
                Nous négligerons cette concentration. L'analyse n'ayant qu'une valeur indicative ne peut servir de mesure.<br>                       
                
