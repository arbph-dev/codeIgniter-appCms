            <div>
                <div>
                Ces chaudières étaient assez répandue sur le parc<br/>
                Briec / Finistère Habitat / Les Lilas  : 2 générateurs<br/>
                Le Faou / Kan Ar Mor / FPA   : 2 générateurs<br/>
                <br/>

                <h3>Entretien annuel</h3>
                La documentation Atlantic de qualité permet de réaliser l'entretien des chaudières sans difficultés<br/>
                <ul>
                    <li>Remplacement du filtre air comburant</li>
                    <li>Contrôle des électrodes, remplacement si nécessaire</li>
                    <li>Nettoyage et contrôle du siphon à condensats</li>
                    <li>Ouverture du circuit de fumée , nettoyage</li>
                    <li>Contrôles des sécurités</li>
                    <li>Contrôle de combustion</li>                    
                </ul>
                
                L'entretien de la chaufferie comporte :<br/>
                <ul>                
                    <li>controle du vase d'expansion</li>
                    <li>nettoyage des pots à boue et des filtres</li>
                    <li>ramonage des conduits de fumée</li>
                    <li>rédaction du certificat 4/400</li>
                    <li>contrôle des régulations et asservissements</li>
                    <li>contrôle ligne gaz : coupure, étanchéité et détection gaz</li>
                    <li>Relever les compteurs avant, après ; pression et top gaz</li>
                </ul>

                Les entretiens annuels sont à documenter dans la gmao, celle ci permet d'inclure les photos aux rapports.<br/>
                Ces rapport visent à justifier du travail réalisé et permettent de produire les attestations d'entretien.<br/>
                <br>
                Ces rapports d'intervention permettent également de conserver les références des pèces pour les approvisionnements ultérieurs. 
                <br>                

                <h4>Remplacement du filtre air comburant</h4>
                Le détail de l'opération est expliqué dans la documentation.<br>
                La dépose repose ne pose pas de difficulté hormis la précaution nécessaire avec les tôles fines et coupantes.
                un kit média est fourni par Atlantic

                <h4>Contrôle des électrodes</h4>
                On contrôle l'état de l'isolant de électrodes, l'isolant est correct.<br>
                Les électrodes sont de bonne épaisseur un frottage à la brossage métallique puis un papier de verre fin permettront de les remettre en place<br>

                <h4>Nettoyage et contrôle du siphon à condensats</h4>
                Le siphon est accessible par une trappe latérale<br>
                Le siphon est démonté et nettoyé<br>
                On remplit d'eau pour l'étanchéité, et on contrôle que la boule fasse office de clapet 

