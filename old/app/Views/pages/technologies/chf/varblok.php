            <h2>Entretien annuel</h2>
            <div>
                <div>
                    Arrivé en pleine saison de chauffe, l'entretien annuel fut l'occasion de maitrîser davantage ses équipements. 

                    <h3>Chaufferie</h3>
                    On débute par l'analyse de l'eau ; on prélève l'eau des réseaux pour analyse.<br/>
                    Le vase d'expansion est isolé, purgé puis gonflé.<br/>
                    <br/>
                    Un piquage présente des traces de fuite à la dépose il s'avèrera qu'il est percé.<br/>
                    N'ayant pas de filière je n'ai pu reprendre le filetage, j'ai pris du téflon large et ramené la vanne au maximum en arrière.<br/> 
                    Aucune fuite n'a été constatée à la remise en pression ni par la suite ,le rapport préconise cependant une réparation durable.<br/>
                    <br/>
                    Le bac a condensats est a nettoyer, on fait l'appoint de bille neutralisante.<br/>
                    
                    <h3>Chaudières</h3>
                    Les chaudière sont isolées : on coupe l'alimentation électrique et on ferme les vannes gaz<br/>
                    <br/>
                    
                    <h3>Ouverture cote feu</h3>
                    On déconnecte la prise de pression, on dessere le collier et on dépose le filtre à air du bruleur<br/>
                    On dépose le média filtrant et on le remplace<br/>
                    <br/>                  
                    On débranche les connecteurs sur le ventilateur, le bloc gaz, l'allumeur et la ionisation<br/>
                    <br/>
                    Dépose de la ligne gaz; on dévisse les quatre vis pour retirer le flexible gaz.<br/>
                    On desserre l'écrou, on dépose le brûleur et on accéde à la chambre de combustion<br/>
                    
                    <h3>Entretien bruleur</h3>
                    L'allumeur et la sonde de ionisation seront remplacés ainsi qu'une une cosse en piteux état. <br/>
                    j'ai tester avec et sans pour ma culture perso, la chaudière dispose de prise de masse redondantes, l'emplacement choisi pour cette cosse n'est peut être pas le plus approprié; le fil est du fil standrad non siliconé.<br/>
                    on dépose joint , electrode et allumeur sur le bruleur<br/>
                    On netoie les portées puis on met les nouvelles pieces<br/>
                    
                    <h3>Nettoyage chambre de combustion</h3>
                    Le nettoyage de la chambre , brosse et jet d'eau<br/>
                    Un petit controle sur l'évacuation des condensats après le nettoyage<br/>

                    <h3>Fermeture cote feu</h3>
                    on repose le bruleur<br/>
                    on remonte la ligne gaz<br/>
                    on rebranche les connecteurs<br/>
                    on remonte le filtre <br/>
                    on connecte la prise de pression<br/>

                    <h3>Essais de fonctionnement</h3>
                    on coupe l'armoire pour vérifier l'éclairage de sécurité<br/>
                    on alimente les chaudières en élécrécité, on ouvre les vannes sur le reseau<br/>
                    on vérifie le fonctionnement des 2 chaudieres<br/>
                    un cycle a blanc sans gaz, puis on ouvre le barrage gaz<br/>
                    on fait les contrôles de combustion<br/> 
                    Idéalement on vérifie la puissance avec un top gaz<br/>

                    <h3>Fin d'entretien</h3>
                    On remet les chaudières en service<br/>
                    Relevés des index : eau et gaz<br/>
                    On remplit le cahier de chaufferie<br/>
                    On saisit le rapport<br/>

