                        Lors d'essais j'ai forcé les relais d'interface et vérifier la prise en compte par le transmetteur LORA, le transmetteur percevait bien l'alarme mais le routeur ne les réceptionnait pas.<br/>
                        Le routeur disposait d'un port USB mais sans logiciel, ni support, j'ai transmis ma demande au service compétent.<br/>
