                    <p>
                    Malgré les difficultés, j'ai effectué le remplacement des joints du collecteur.<br/>
                    La pose des nouveaux joints, plus rigides, a nécessité des ajustements précis pour positionner correctement le collecteur.<br/>
                    La chaudière a été mise en service. Les contrôles finaux ont confirmé l'absence de fuite, et le problème a été définitivement résolu.<br/>
                    <br/>
                    On disposait de pièces en stock, celles ci furent d'autant plus utiles qu'on ne pouvait plus en approvisionner. Je dut remplacer un ventilateur en fin de saison, quelques jours après un autre commençait à faire du bruit.<br/>
