            <h2>Signalement et préparation de l'intervention</h2>
            <div> 
                <div>
                    <p>
                        Conscient des risques, j'ai immédiatement ventilé puis entrepris une recherche de fuite depuis le barrage extérieur jusqu'à la chaudière; en parallèle d'un controle d'étanchéité.<br> 
                        <br>
                        Malgré une inspection minutieuse des raccords, aucune fuite n'a été détectée dans ces zones, le manomètre n'as pas chuter non plus.<br>
                        <br>  
                        Ces actions n'ont rien révélé car le problème se situait en aval de la vanne gaz principal, l'odeur n'était pas même perceptible en fonctionnement.<br>
                        En approfondissant mes recherches, j'ai constaté qu'un goujon maintenant le collecteur de mélange était dessoudé. Le mille bulles ne révélait rien mais un détecteur aurait été necessaire<br>
                        <br>
                        Le local était ventilé, du fait qu'une panne totale soit survenue, J'ai alerté mon chef d'agence pour organiser l'intervention d'un soudeur spécialisé.<br>
                        Nous avons considéré que la fuite ne présentait pas de danger immédiat<br>
                        <br>
                        Ne connaissant pas ce type de chaudière, j'ai sollicité l'assistance d'un collègue expérimenté pour déposer le collecteur.<br>    
                        <br>
                        Avant l'arrivée du soudeur, nous avons démonté les éléments nécessaires. Lors de cette étape, j'ai remarqué l'usure avancée des joints et suggéré leur remplacement.
                        Mon collègue, fort de 20 ans d'expérience, m'a assuré que ce n'était pas nécessaire.    
                        <br/>                        
                        Le soudeur est intervenu pour réaliser une soudure acier sur fonte en position, tout en partageant des conseils pratiques pour le graissage des goujons et le maniement des boulons.<br>
                        <br>
                        Une fois la soudure effectuée, nous avons remonté l'ensemble de la chaudière et vérifié son fonctionnement.<br>
                        Aucune fuite n'a été constatée, et nous avons conclu à une résolution du problème.
                </p>

