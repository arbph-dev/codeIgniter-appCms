            <h2>Analyse technique et contexte humain</h2>
            <div> 
                <div>
                    <p>
                    L'analyse de la situation a révélé que l'odeur était liée à un défaut d'étanchéité du collecteur de mélange, situé en aval du bloc gaz. 
                    Ce problème n'était perceptible qu'en phase de fonctionnement. Cette intervention a mis en lumière des enjeux humains et organisationnels :<br/>
                    <b>Reconnaissance des compétences</b> :Bien que responsable du chantier, j'ai dû composer avec des résistances de la part de collègues aguerris, parfois réticents aux propositions d'amélioration.<br/>
                    <b>Pression liée aux responsabilités</b> : Intervenir sans habilitation gaz, sur un équipement inconnu, a renforcé ma vigilance quant à la sécurité et à l'importance des bonnes pratiques.<br/>
                    </p>

