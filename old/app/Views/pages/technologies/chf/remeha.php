                    <h3>Prise en charge</h3>
                    <p>
                        Pour commencer un état des lieux fut nécessaire, le câblage de l'ancienne GTC est toujours présent, les boites de dérivation également<br/>
                        Il faut donc identifier le câblage fonctionnel, d'un coffret à l'autre on retrouve le câble téléphonique. j'ai déconnecter les anciens conducteurs au borniers et supprimer les câbles du coffret<br/>
                        <br/>
                        Une passerelle LORA/Modbus est connecté à un automate SOFREL disposant d'une liaision GSM, la télégestion n'a pas d'erreur de test cyclique.<br/>
                        Le SOFREL communiquait mais la liaison LORA ne fonctionnait malheureusement pas<br/>
