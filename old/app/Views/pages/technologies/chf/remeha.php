            <h2>Défaut afficheur</h2>
            <div> 
                <div>
                    <p>
                    Au retour de weekend, par un temps heureusement clément, le report de défaut étant une horreur. Une des six chaudières du quartier est en défaut<br/>
                    <br/>
                    La température sur les thermomètre est correcte mais une sonde de la chaudière semble renvoyer une valeur incohérente.<br/> 
                    La température de retour > T aller doit déclencher une alarme après une temporisation. Ce n'est pas le cas , l'électronique semble en défaut.<br/>
                    Un autre problème malgré deux remise sous tension l'afficheur ne répond plus.<br/>
                    </p>

                    <p>
                    Compte tenu que:
                        <ul>
                        <li>l'installation nécessiterait un nouveau passage en cas d' un défaut de sonde ou de carte d'entrée</li>
                        <li>La sonde et cartes ont été payés par le client dans le cadre du contrat</li>
                        <li>nous arrivons en fin de saison et que le client a prévu de refaire le chaufferies</li>
                        </ul>
                    je décide de remplacer la sonde de retour, les cartes afficheur et entrées
                    </p>


                    <h3>Remplacement des pièces</h3>
                    <h4>Sonde de température retour</h4>
                    <p>on vérifie la mesure de la sonde en stock , la mesure à l'ohmmètre est correcte et correspond à la température.<br/>
                    On raccorde la sonde, elle sera monté dans un de doigt de gant après les essais de remise en service. 
                    </p>
                    <h4>Cartes afficheur et entrées</h4>
                    <p>
                    La chaudière comporte 4 cartes :
                    <ul>
                        <li>une carte alim au fond a gauche</li>
                        <li>une carte d'entrée au fond a droite</li>
                        <li>l'afficheur au premier plan</li>
                        <li>un boitier comprenant alimentation et bornier</li>
                    </ul>
                    on a  la carte d'entrée avec une pastille 2017 et la carte afficheur avec une pastille 2021 (date de  réparation ??)
                    </p>

                    <h3>Paramétrage de la carte</h3>
                    <p>Petit moment de solitude, on a un manuel assez explicite. Un tableau apposé sur la chaudière permet de retrouver les paramètres
                    </p>

                    <h3>Essais</h3>
                    <p>La chaudière s'inistialise et se remet en service pour un essai à blanc. On constate le fonctionnemt du pressostat mini gaz.<br/>
                    On installe la nouvelle sonde dans son emplacment<br/>
                    <br/>
                    la chaudière redémarre après un essai à blanc. C'est fini</p>




