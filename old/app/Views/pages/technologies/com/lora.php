        <section>
            <h2>Maintenance réseau LORAWAN</h2>
            <div> 
                <div>
                    <p>
                        Les réseaux LORA nécessite quelques interventions de maintenance.<br/>  
                        Un client, bailleur social, à dû insisté  pour faire remettre en état ces équipements, équipements qui lui ont été facturés tout comme son entretien dans le cadre du P3.<br/> 
                        <br/>
                        On m'a bombardé réfèrent sur la technologie sans avoir aucune information ni documentation et encore moins de logiciel, j'ai apprécié mon implication.<br/> 
                        
                        <h3>Etat des lieux</h3>
                        On avait attaché peu d'intérêt à ces outils payés par le client, des localisations étaient incorrectes.<br/> 
                        Lors de défaut de chaudière; 12 pour 4  bâtiments il est apparu que des sondes indiquées pour un bâtiment se trouvaient sur une autre chaufferie.<br/> 
                        Dans d'autres quartiers, des équipements et même un réseau ne fonctionnaient pas faute de paramétrage.<br/>
                        <br/>  
                        Ces outils sont réellement utiles, ils permettent de gérer les urgences et évite de déranger les gens comme les techniciens pour des contrôles de température.<br/>
                        <br/>
                        Les pompiers sont là pour gérer les urgences.<br/>
                        Les techniciens sont là pour éviter les pannes. Les automatismes et la régulation sont les meilleur ami du technicien.<br/>
                        <br/>
                        Il est préférable d'agir avant qu'on vous sollicite.<br/>
                        Un client, même mécontent, appréciera de vous trouver sur ces équipements,  a régler ses problèmes plutôt que de devoir utiliser son téléphone quand les technologies que l'on lui aura vendu sont censés vous prévenir.<br/>
                        <br/> 
                        L'inertie thermique des bâtiments permet d'éviter de nombreuses tensions<br/>
                        La reprise sur défaut permet de différer les interventions faut il encore que les equipements de secours fonctionne.<br/>
                        Le suivi des températures, le relevé des index gaz et énergie, le réglage des courbes peut se faire a distance. Ce sont des outils, comme le tournevis, indispensables dés lors que l'on exige une qualité de service<br/>
                        <br/>
                        Ce manque de gestion, impactant le suivi et le réglage des courbes de chauffe est apparue sur d'autres chantiers; chantiers sur Ergue Gaberic que nous allions perdre pour ses raisons.

                        
                        <h3>Remplacement des batteries</h3>
                        Les équipements du réseau sont alimentés par batterie, celles ci doivent etre remplacées après une dizaine d'années.<br/>
                        La principale difficulté fut l'absence de documentation et de localisation des équipements plus particulièrmeent ceux posés chez les locataires.<br/>
                        <br/>
                        La majorité des boitiers a fonctionné après remplacement des batteries, cependant certains boitiers placés en éxtérieure ont été endommagé par l'humidité

                        <h3>Remplacement d'une alimentation</h3>
                        Le routeur LORA du quartiers les chardons bleus de Crozon ne fonctionnait plus, son bloc d'alimenation ne délivrait plus de tension.<br/>
                        J'ai utilisé un bloc d'alimentation multitension, personnel, pour rétablir la liaision.<br/>
                        <br/>
                        Ce matériel personnel est resté en  place pour 6 semaines.<br/>
                        La réparation coutera 4 fois le prix, ell nécessitera l'ajout d'un différentiel et d'une prise dans l'armoire; j'avais pourtant fourni la référence et trouver une équivalence chez Radiospares.<br/>
                        Selon le service achat le matériel télécom dépendait du service compétent, l'approvisionnement chez Radiospares n'était pas une option.<br/>
                        <br/>

                        <h3>Réseau mal paramétré</h3>
                        Suite à des défauts non remontés ou mal localisés par la télégestion j'ai vérifié les équipements Enless sur plusieurs quartiers.<br/>
                        Sur le quartier de park Ar Roz de Briec de l'Odet ou une panne totale était survenue j'ai vérifié le fonctionnement de la GTC en liaison avec un collègue de Rennes<br/>
                        <br/>
                        Les transmissions se font sur évènement on visualise l'état au moyen de voyants; faute de documentation il était difficile d'interpréter les autres signaux lumineux.<br/>
                        Le repérage en extérieure s'est avéré beaucoup plus délicat.<br/>
                        Après essais, il est apparu qu'aucune alarme ne remontaient depuis un quartier de Briec; sans possibilité de modifier le paramètrage des SOFREL et routeur LORA j'ai transmis les anomalies au service compétent.
                        
                        <h3>Remplacment de sondes connectées</h3>
                        Nous ne pouvions plus approvisionner les sondes Enless pour les relevés de température intérieure, un nombre de télérévés contractuels imposait de rempalcer les sondes qui ne fonctionnaient plus ou que l'on ne retrouvait pas.<br/>
                        Le choix de la gestion du réseau s'est porté sur une solution opérateur de téléphonie plutôt que le réseau local, la télégestion pouvant gérer plusieurs canaux de communication pour regrouper les données<br/>
                        <br/>
                        La pose de nouveaux équipements ne posait pas de difficultés techniques, un collègue de Rennes nous a expliqué la procédure<br/>
                        Lors d'une livraions de bois sur Crozon j'ai entrerpis de poser une nouvelle sonde connectée, comme demandé sur le BT.<br/>
                        <br/>
                        Pour enregistrer la sonde connectée; il y avait une application.<br/>Je pu en installer au quartier des chardons bleus, mais pas aux Ajoncs d'or distant de 700 m.<br/>
                        J'ai complété le BT, téléphoné au référent des référents.<br/>
                        Je n'ai eu aucun retour; j'ai essayé à plusieurs reprises, sans succès. l'obstacle n'était pas technique<br/>
                    </p>
