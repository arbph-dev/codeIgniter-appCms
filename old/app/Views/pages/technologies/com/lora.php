            <h2>Les équipements du réseau LORAWAN</h2>
            <div> 
                <div>
                    <p>
                        En tête du reseau local on trouve l'automate SOFREL qui peut concentrer les communications de plusieurs réseaux LORA
                        Chaque réseau LORA disposant de son routeur, un reseau température peut coexister avec un reseau contact. 
                        Un routeur gères des capteurs, les capteur sont de sondes de température, des compteurs Gaz et MBUS , des entrées et des sorties
                        <br/>
                    </p>
                    <h3>Réseau LORA</h3>
                    Le routeur LORA sert de passerelle entre son réseau local et le réseau d'automate, ils communiquent via une liaison modbus.<br/>
                    L'automate peut interroger le routeur pour récuperer les données, les stocker et les transmettre sur une liaison GSM<br/>
                    <br/>
                    Les périphériques sont associés au routeurs, les transmissions se font cycliquements sur le reseau local, le maitre ne peut interroger les esclaves directement.<br/>
                    Les périphériques transmettent régulièrment les données, c'est le seul moment ou le maitre peut intéragir avec l'esclave.

                    <h3>Périphériques</h3>
                    Les entrées et sorties numériques, les sondes de température , les impulsions des compteurs gaz sont gérés par les esclaves du reseau LORA<br/>
                    Chaque équipement possède un code figurant plus ou moins lisible sur l'étiquette.<br/>
                    <br/>
                    Une attention particulière doit etre porté lors de la pose de ces équipements, les échanges de libellés seront évités et les codes des équipements seront associés au libellés et archivés.<br/>
                    <br/>



