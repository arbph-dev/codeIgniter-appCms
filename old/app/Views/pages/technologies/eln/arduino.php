                <h2>Shield</h2>
                <div>
                    <div>
                        <h3>Shield Ethernet W5100 Carte SD</h3>
                        <p>
                            Les shields sont des cartes enfichables permettant d'étendre les fonctions de l'Arduino Mega<br>
                            La version actuelle est disponible<a href="https://store.arduino.cc/products/arduino-ethernet-shield-2" target="_blank">ici</a><br>
                            <br>
                            Cette carte permet de disposer d'une interface ethernet compatible POE et une carte SD.<br>
                            <br>
                            Cette carte m'a permit de réaliser un esclave ModbusTCP.<br>
                            Un projet de passerelle Modbus viendra compléter cette rubrique.
