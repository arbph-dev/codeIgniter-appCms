                <h2>Arduino MEGA</h2>
                <div>
                    <div>
                        <h3>ATmega2560</h3> 
                        Ce composant dispose de 100 Broches, il est compatible avec les niveaux TTL.<br>
                        <br>
                        L'ATmega2560 est un microcontonleur 8 bits, les broches d'entrées et sorties sont réparties sur des ports.<br>
                        Les broches peuvent avoir plusieurs fonctions, fonctionner en entrées ou en sortie selon la configuration retenue.<br>
                        <br>

                        <h4>Caracétristiques de l'Arduino Mega</h4>
                        Tension de fonctionnement 5V<br>
                        Broche d'entrées sorties : 54<br>
                        Entrées analogiques	16<br>
                        Memoire Flash 256 KB ,SRAM 8 Ko, EEPROM 4 Ko<br>
                        Fréquence d'horloge 16 MHz<br>
                    </div>
