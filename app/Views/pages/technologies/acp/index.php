                    <h3>Comptage</h3>
                    L'interface homme machine du compresseur permet de relever :
                    - Date et heure du relevé<br>
                    - Heure régulateur : machine sous tension<br>
                    - Démarrage<br>
                    - Heure Marche : moteur en service<br>
                    - Heure Charge : compresseur débite sur le réseau<br>
                    <br>
                    Ces compteurs permettent de mettre en place 3 indicateurs<br>
                    - La fréquence de démarrage <br>
                    - Engagement de la machine, rapport  du temps de marche sur temps sous tension<br>
                    - Taux charge : rapport  du temps en charge sur temps de marche<br>
                    <br>
                    On peut estimer :<br>
                    - Volume d'air produit depuis les heures de marche et débit corrigé<br>
                    - Energie absorbée depuis les puissances et heure de marche et en charge<br>
                    - Ratio de production en Wh / m3<br>
                    <br>
                    Selon l'importance de la station de production on instumente le réseau pour surveiller et optimiser la production.<br>



