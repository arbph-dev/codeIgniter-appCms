                    <h3>Economie d'Energie</h3>
                    Dans le cadre des économies d'énergies des vannes pneumatiques, avec bypass, ont été posées sur les départs d'atelier du site de SPONTEX
                    Le planning était modifiable sur demande écrite depuis le réseau des automates de chauffage.
                    Pour cela nous avons réalise les raccordements pneumatiques et électriques puis modifié le programme des automates iq3

                    <h4>Pression réseau</h4>
                    La pression du réseau doit être ajusté parfois à discrétion pour éviter les diagnostiques rapides. On tient compte des pertes sur le réseau de distribution pour régler les consignes des compresseurs.
                    En  pratique on descend par palier en prévenant les équipes de production, on recherchera  la pression critique avec la maintenance puis  on ménage une marge de sécurité.

