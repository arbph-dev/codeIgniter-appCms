            <h2>EOLANE -  Suppression du sécheur à absorption</h2>
            <div>
                <div>
                    En charge des 5 bâtiments du site, au service généraux je devais déléguer certains interventions et encadrer les intervenants. Dans ces activités on doit disposer de ressources matérielles, de compétences techniques et humaines.  On apprend a demander aux gens ce qu'ils savent faire mais il faut les connaitre.
                    <br>
                    Le sécheur à absorption n'avait pas été entretenu, le disséquant était a remplacer. Ce sécheur fonctionnait en recyclant l'air de refroidissement, cette solution est adaptée au machine qui fonctionne en permanence.<br>
                    Le réseau ne justifiant pas d'investissement lourd, les purgeurs fonctionnaient nous avons supprimé le sécheur et ajouter la purge des ballons tampons au ronde.<br>

                    <h3>Préparation</h3>
                    En présentant le chantier j'ai été précis sur le délai réduit d'intervention et la difficulté de la tache. <br>
                    Une manchette filetée devait être réalisée ,Les raccords unions corrodées devaient être déposés et remplacés .<br>
                    <br>
                    Sur ce site je ne disposais pas de filière, mais j'ai stocké des raccords et des manchons de réparation pour le réseau sprinkler.<br>
                    
                    <h3>Intervention</h3>
                    La machine est consigné électriquement , isolé et purgé.<br>
                    <br>
                    L'intervention débute, la corrosion avancée à soudé les raccords la clef a griffe ne suffit pas<br>
                    Plusieurs méthodes éprouvées peuvent être employées : <br>
                    - Le dégrippant a action cryogénique donne de bons résultats  mais nous n'en disposions pas<br>
                    - La disqueuse et le burin aurait permis de déposer l'ensemble mais les projections de métal en fusion et la poussière sont à éviter dans ces locaux (graisse et huile, filtre à air). En outre il faut désactiver la protection incendie ce qui retarde l'intervention
                    <br>
                    - Un tube en rallonge peut augmenter le couple mais nous n'en avions pas.<br>
                    - Les chocs d'un martelage énergique ont permis de désolidarisé le raccord 3 pièces, les vibrations répétées ont permis de casser les soudures. <br>
                    <br>
                    J'ai donc résolu rapidement la principale difficulté avec quelques efforts.<br>
                    Lors de la dépose des raccords il apparait que les raccords approvisionnés n'était pas du bon type <br>
                    L'intervenant propose de retourne les cherche a son atelier. Nous le lassions préparer les filetages et nous allons au local maintenance récupérer des raccords.<br>
                    <br>
                    Nous terminons l'intervention dans les délais.
