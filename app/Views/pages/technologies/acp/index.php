			<h2>SPONTEX - Description</h2>
			<div>
				<div>
                    La centrale comporte :<br>
                    - compresseur à vitesse variable à vis sèche : ZT 250 VSD<br>
                    - compresseur à vis sèche : ZT 200<br>
                    - 2 compresseurs Ingersoll à vis lubrifié <br>
                    - sécheur frigorique FD1250<br>
                    - 2 sécheur frigorique Chauméca<br>
                    - filtre double<br>
