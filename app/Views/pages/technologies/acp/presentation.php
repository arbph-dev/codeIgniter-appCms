        <section>
            <h2>Principe physique de la compression</h2>
            <div>
                <div>
                    La compression d'un gaz est régie par les lois de la thermodynamique, notamment la relation pression-volume-température.<br/>
                    Plus le taux de compression est élevé, plus la température augmente.

                    <h3>Types de compression</h3>
                    On disitngue : 
                    <ul>
                    <li>compression Isotherme</li>
                    <li>compression Adiabatique</li>
                    <li>compression Polytropique</li>
                    </ul>

                    <h4>compression Isotherme</h4>
                    c'est le cas idéal, la compression se ferait à température constante avec un rendement optimal
                    <h4>compression Adiabatique</h4>
                    La compression se fait sans échange thermique, la température s'élève fortement                                        
                    <h4>compression Polytropique</h4>
                    C'est la compression la plus courante, elle necessite compression multi-étagée et refroidissement intermédiaire.


                </div>
                <aside>
                    <strong>PV = nRT</strong>
                    P : pression en Pascal Pa<br/>
                    V : volume<br/>
                    T : température en Kelvin K<br/>
                    nR : constante<br/>
                </aside>                
