        <section>
            <h2>Technologies des compresseurs</h2>
            <div>
                <div>
                    Ils existent plusieurs technologies de compresseur. On choisit la technologies à mettre en oeuvre selon les applications :<br/>
                    On disitngue :<br/>
                    <ul>
                    <li>Compresseurs à pistons</li>
                    <li>compression Adiabatique</li>
                    <li>compression Polytropique</li>
                    </ul>                    
                    

                    <h3>Compresseurs à pistons</h3>
                    La Compression se fait par déplacement alternatif du piston, selon un principe analogue aux moteurs thermique. Les pistons sont animés par un moteur externe. Des clapets permettent d'introduire et de refouler l'air comprimé.<br/>
                    Les compresseurs à pistons permettent de fournir de l'air comprimé à très haute pression ( > 400 bar ) mais à faible débit. Ils ont l'inconvénient dêtre bruyants, d'engendrer des vibrations importantes et de nécessiter une maintenance conséquente.<br/>
                    Les applications sont principalement la compression de gaz en bouteille<br/>
 
                    <h3>Compresseurs à vis</h3>
                    Deux rotors hélicoïdaux compressent l'air en continu. Le profil des vis induit la réduction de l'espace entre les vis, le volume diminue la pression augmente.<br/>
                    Les compresseurs à vis sont les plus répandues dans l'industrie, ils fournissent des pressions jusqu'a 13bar avec des débits importants.<br/>
                    On distingue 2 technologies à vis lubrifiées pour un usage général ou à vis sèches pour des applications plus sensibles. Les compresseurs à vis nécessitent moins d'entretien et sont plus silencieux.<br/>

                    <h3>Compresseurs centrifuges</h3>
                    Une roue ou rotor entrainé à haute vitesse provoque une compression dynamique en accélérant l'air qui est expulsé dans le diffuseur.<br/>
                    Ces machines utilise la force centrifuge pour comprimer l'air, cette compression modéré necessite plusieurs étages de compression mais permet de délivrer des débits conséquents.<br/>
                    Les applications sont les industries lourdes qui nécessite la production d'énergie.<br/>


