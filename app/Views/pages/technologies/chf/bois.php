        <h2>EPHAD Kerval - Exploitation dégradée</h2>

        <div>
            <div>
            <h3>Bourrage chaudière</h3>
            <p>Accumulation de mâchefer et présence de terre dans le combustible.<br/>
            Conséquences :
            </p>
            <ul>
                <li>tirage insuffisant</li>
                <li>grille bloquée</li>
                <li>arrêt chaudière</li>
            </ul>

            <p>
            Nettoyage complet nécessaire avec précautions sur les réfractaires.
            </p>

            <h3>Alimentation bloquée</h3>
            <p>
            Blocage de vis sans fin par corps étranger.<br/>
            Remise en service après extraction.
            </p>

            <h3>Qualité combustible</h3>
            <p>
            Mise en évidence d'un problème d'hygrométrie et de pollution du bois.<br/>
            Sensibilisation du client + préconisation fournisseur.
            </p>

            <h3>Vis cassée</h3>
            <p>
            Fatigue mécanique suite aux blocages répétés.<br/>
            Réparation en atelier puis remise en service rapide.
            </p>

            <p>
            La cause racine reste la qualité du combustible.<br/>
            Sans correction de ce point, les défaillances persistent.
            </p>

